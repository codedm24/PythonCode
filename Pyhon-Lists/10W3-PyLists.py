# List - ordered changeable indexed
# allow duplicate values

mylist = ["apple", "banana", "cherry"]
print(mylist)

mylist = ["apple", "banana", "cherry", "apple", "cherry"]
print(mylist)
print(len(mylist))

list2 = [1, 5, 7, 9, 3]
list3 = [True, False, False]
print(list2)
print(list3)

list1 = ["abc", 34, True, 40, "male"]
print(list1)
print(type(list1))

thislist = list(("apple","banana","cherry"))
print(thislist)
print(type(thislist))